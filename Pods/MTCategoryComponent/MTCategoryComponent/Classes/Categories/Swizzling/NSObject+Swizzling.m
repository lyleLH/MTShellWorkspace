#import "NSObject+Swizzling.h"

@implementation NSObject (Swizzling)

+ (BOOL)mt_exchangeInstanceMethod:(SEL)sel1 withMethod:(SEL)sel2{
    Method m1 = class_getInstanceMethod(self, sel1);
    if (!m1) return NO;
     
    Method m2 = class_getInstanceMethod(self, sel2);
    if (!m2) return NO;
     
    if (class_addMethod(self, sel1, method_getImplementation(m2), method_getTypeEncoding(m2))) {
        class_replaceMethod(self, sel2, method_getImplementation(m1), method_getTypeEncoding(m1));
    }
    else {
        method_exchangeImplementations(m1, m2);
    }
     
    return YES;
}

@end
