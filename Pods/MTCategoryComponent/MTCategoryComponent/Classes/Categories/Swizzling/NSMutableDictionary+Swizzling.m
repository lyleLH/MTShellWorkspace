#import "NSMutableDictionary+Swizzling.h"
#import "NSObject+Swizzling.h"

@implementation NSMutableDictionary (Swizzling)

+ (void)load{
    [objc_getClass("__NSDictionaryM") mt_exchangeInstanceMethod:@selector(setObject:forKey:) withMethod:@selector(mt_setObject:forKey:)];
}

- (void)mt_setObject:(id)anObject forKey:(id<NSCopying>)aKey {
    if (anObject && aKey) {
        [self mt_setObject:anObject forKey:aKey];
    }else{
        NSString *tip = @"字典 nil crash";
        #ifdef DEBUG
        NSAssert(0, tip);
        #endif
        NSLog(tip);
    }
}

@end
